type testVec struct {
	dir int
	key []byte
	tweak []byte
	in []byte
	out []byte
}

func verifyTestVec(vec testVec, t *testing.T) {
	out := EncryptEME32(vec.key, vec.tweak, vec.in)
	if bytes.Compare(vec.out, out) != 0 {
		t.Errorf("Different content")
	}
}

